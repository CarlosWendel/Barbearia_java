    private void preencheTable(ArrayList<Agendamento> allAgenda) {
        //(int id, Cliente cliente,Servico servico,  double valor, String data,String hora,String observacao)
        DefaultTableModel dftm = (DefaultTableModel) jTableAgendamento.getModel();
            while(dftm.getRowCount() >0){
                dftm.removeRow(0);
            }
        for(Agendamento  age: allAgenda){
            String[] linha = {age.getId()+"",age.getCliente().getNome()+"",age.getServico().getDescricao()+"",age.getValor()+"",age.getData()+"",
                age.getHora()+"",age.getObservacao()+""};
            dftm.addRow(linha);
        }
        
    }

    
