    private void jButtonApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarActionPerformed
        int row= jTableAgendamento.getSelectedRow();
        DefaultTableModel model = new DefaultTableModel();
        if(row <= -1){
            model.removeRow(row);
            
        }else{
            
            
            if(JOptionPane.showConfirmDialog(null,
                    "Apagar o agendamento do cliente "+ allAgenda.get(row).getCliente().getNome()+"?",
                    "Deseja apagar o agendamento  ?",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                
                try {
                    new AgendamentoDAO().apagar(allAgenda.get(row).getId());
                } catch (ClassNotFoundException |SQLException ex) {
                      JOptionPane.showMessageDialog(null, "Não foi possivel apagar o agendamento ");
                } 
                JOptionPane.showMessageDialog(null, "apagou");
            }
            
        }
         formWindowOpened(null);
    }//GEN-LAST:event_jButtonApagarActionPerformed

